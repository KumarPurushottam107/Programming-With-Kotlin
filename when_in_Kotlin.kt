fun main() {
    
   // when expression :- It is similar to switch state in c++, Java
   
   var name : String  = "Avinash" 
    
    when(name)
    {
        "Amri" -> println("Dedication and Smartness")
        
        "Avinash" -> println("Symbol of Power")
        
        "Ashutosh" -> println("Sharp and Humorous")
        
    }
    
    // Output will be Symbol of Power
    
    
    
   
   
}
